    <script>
        // Terms toggle functionality
        function toggleTerms(element) {
            const content = element.nextElementSibling;
            const icon = element.querySelector('.terms-icon');
            
            // Close all other sections
            const allContent = document.querySelectorAll('.terms-content');
            const allIcons = document.querySelectorAll('.terms-icon');
            
            allContent.forEach((cont, index) => {
                if (cont !== content) {
                    cont.classList.remove('active');
                    allIcons[index].classList.remove('rotated');
                }
            });
            
            // Toggle current section
            content.classList.toggle('active');
            icon.classList.toggle('rotated');
        }

        // Back to top functionality
        function scrollToTop() {
            window.scrollTo({top: 0, behavior: 'smooth'});
        }

        // Show/hide back to top button
        window.addEventListener('scroll', function() {
            const backToTop = document.querySelector('.back-to-top');
            if (window.pageYOffset > 300) {
                backToTop.classList.add('visible');
            } else {
                backToTop.classList.remove('visible');
            }
        });

        // Add keyboard navigation for accessibility
        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape') {
                // Close all open sections
                const allContent = document.querySelectorAll('.terms-content');
                const allIcons = document.querySelectorAll('.terms-icon');
                
                allContent.forEach((cont, index) => {
                    cont.classList.remove('active');
                    allIcons[index].classList.remove('rotated');
                });
            }
        });

        // Initialize page
        document.addEventListener('DOMContentLoaded', function() {
            // Add focus styles for keyboard navigation
            const focusableElements = document.querySelectorAll('button, [tabindex]');
            focusableElements.forEach(element => {
                element.addEventListener('focus', function() {
                    this.style.outline = '2px solid #4f46e5';
                    this.style.outlineOffset = '2px';
                });
                
                element.addEventListener('blur', function() {
                    this.style.outline = 'none';
                });
            });
        });
    </script>
