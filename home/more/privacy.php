    <script>
        function togglePrivacy(element) {
            const answer = element.nextElementSibling;
            const icon = element.querySelector('.privacy-icon');
            
            const allAnswers = document.querySelectorAll('.privacy-answer');
            const allIcons = document.querySelectorAll('.privacy-icon');
            
            allAnswers.forEach((ans, index) => {
                if (ans !== answer) {
                    ans.classList.remove('active');
                    allIcons[index].classList.remove('rotated');
                }
            });
            
            answer.classList.toggle('active');
            icon.classList.toggle('rotated');
        }

        function scrollToTop() {
            window.scrollTo({top: 0, behavior: 'smooth'});
        }

        window.addEventListener('scroll', function() {
            const backToTop = document.querySelector('.back-to-top');
            if (window.pageYOffset > 300) {
                backToTop.classList.add('visible');
            } else {
                backToTop.classList.remove('visible');
            }
        });

        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape') {
                const allAnswers = document.querySelectorAll('.privacy-answer');
                const allIcons = document.querySelectorAll('.privacy-icon');
                
                allAnswers.forEach((ans, index) => {
                    ans.classList.remove('active');
                    allIcons[index].classList.remove('rotated');
                });
            }
        });

        document.addEventListener('DOMContentLoaded', function() {
            const focusableElements = document.querySelectorAll('button, [tabindex], .privacy-question');
            focusableElements.forEach(element => {
                element.addEventListener('focus', function() {
                    this.style.outline = '2px solid #4CAF50';
                    this.style.outlineOffset = '2px';
                });
                
                element.addEventListener('blur', function() {
                    this.style.outline = 'none';
                });
            });
        });
    </script>
